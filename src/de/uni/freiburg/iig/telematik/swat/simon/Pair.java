package de.uni.freiburg.iig.telematik.swat.simon;

public class Pair {
	private double mStart;
	private double mEnd;
	
	public Pair(double start, double end) {
		mStart = start;
		mEnd = end;
	}
	
	public double getStart() {
		return mStart;
	}
	
	public double getEnd() {
		return mEnd;
	}
	
	
}
