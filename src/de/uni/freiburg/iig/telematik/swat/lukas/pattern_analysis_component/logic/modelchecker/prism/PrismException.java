package de.uni.freiburg.iig.telematik.swat.lukas.pattern_analysis_component.logic.modelchecker.prism;

public class PrismException extends Exception {

	private static final long serialVersionUID = 5522829485237877100L;

	public PrismException() {
		super();
	}

	public PrismException(String message, Throwable cause) {
		super(message, cause);
	}

	public PrismException(String message) {
		super(message);
	}

	public PrismException(Throwable cause) {
		super(cause);
	}
	
	

}
